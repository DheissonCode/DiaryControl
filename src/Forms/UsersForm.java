    private void jb_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_editActionPerformed
        // TODO add your handling code here:
        if(jt_users.getSelectedRow() == -1){
            JOptionPane.showMessageDialog(null, "Selecione um usu치rio!!", null, JOptionPane.ERROR_MESSAGE);
        }else{
           user = usersTable.get(jt_users.getSelectedRow());
           new EditUserForm(user).show();
           dispose();
        }
    }//GEN-LAST:event_jb_editActionPerformed

    private void jb_delActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_delActionPerformed
        // TODO add your handling code here:
        if(jt_users.getSelectedRow() == -1){
            JOptionPane.showMessageDialog(null, "Selecione um usu치rio!!", null, JOptionPane.ERROR_MESSAGE);
        }else{
            user = usersTable.get(jt_users.getSelectedRow());
           if(JOptionPane.showConfirmDialog(null, "Deseja realmente deletar o usu치rio "+user.getLogin())==0){
               try {
                delete();
                JOptionPane.showMessageDialog(null, "Usu치rio deletado com sucesso!!");
                preencher_jtableTudo();
            } catch (SQLException ex) {
                Logger.getLogger(UsersForm.class.getName()).log(Level.SEVERE, null, ex);
            }
           }
           
            
        }
    }//GEN-LAST:event_jb_delActionPerformed

